  created_at timestamptz not null default now()
);

create table if not exists public.contact_messages (
  id bigint generated by default as identity primary key,
  name text not null,
  email text not null,
  phone text not null default '',
  subject text not null,
  message text not null,
  is_read boolean not null default false,
  created_at timestamptz not null default now()
);

create index if not exists idx_legal_areas_slug on public.legal_areas(slug);
create index if not exists idx_legal_contents_slug on public.legal_contents(slug);
create index if not exists idx_legal_contents_area on public.legal_contents(legal_area_id);
create index if not exists idx_events_date on public.events(event_date);
create index if not exists idx_messages_created_at on public.contact_messages(created_at desc);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_platform_settings_updated_at on public.platform_settings;
create trigger trg_platform_settings_updated_at
before update on public.platform_settings
for each row execute function public.set_updated_at();

drop trigger if exists trg_legal_contents_updated_at on public.legal_contents;
create trigger trg_legal_contents_updated_at
before update on public.legal_contents
for each row execute function public.set_updated_at();

