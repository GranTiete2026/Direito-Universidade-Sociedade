create extension if not exists pgcrypto;

create table if not exists public.admin_profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  name text not null,
  role text not null default 'Administrador',
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create or replace function public.is_admin()
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.admin_profiles ap
    where ap.id = auth.uid()
      and ap.is_active = true
  );
$$;

create table if not exists public.platform_settings (
  id bigint generated by default as identity primary key,
  site_name text not null,
  institution_name text not null,
  hero_title text not null,
  hero_subtitle text not null,
  contact_email text not null default '',
  contact_phone text not null default '',
  city text not null default '',
  primary_color text not null default '#06284A',
  accent_color text not null default '#D79A43',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.legal_areas (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text not null unique,
  description text not null default '',
  icon text not null default '⚖',
  is_active boolean not null default true,
  display_order integer not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.legal_contents (
  id bigint generated by default as identity primary key,
  legal_area_id bigint not null references public.legal_areas(id) on delete restrict,
  title text not null,
  slug text not null unique,
  summary text not null default '',
  body text not null default '',
  source text not null default '',
  is_featured boolean not null default false,
  is_published boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

